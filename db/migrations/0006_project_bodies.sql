## 쓰기와 읽기를 쪼갰다

어드민은 로컬에만 있다. 인터넷 어딘가에 로그인 화면이 떠 있지 않다. 글을 쓰는 건 맥 앞에 앉았을 때뿐이고, 그게 보안 모델 전부다.

배포된 사이트는 읽기 전용이다. Supabase에서 published 글만 내려받아 정적으로 굳힌다. 방문자가 닿는 곳에는 쓰기 경로가 아예 없다.

