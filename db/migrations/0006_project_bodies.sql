## 셀 하나에 케이스 하나

주피터 같은 셀 구조다. 셀 하나가 가설 하나다. Routines 설정을 바꿔 가며 Anthropic API를 직접 호출하고, 결과를 그 자리에 남긴다. 통과한 셀만 markdown으로 빼낸다.

```mermaid
flowchart TD
  A[가설] --> B[셀 실행 · API 호출]
  B --> C{기대대로?}
  C -->|예| D[markdown export]
  C -->|아니오| E[조건 바꿔 재실행]
  E --> B
  D --> F[블로그 글]
```

검증 안 된 건 글로 안 옮긴다. 이게 규칙이다. "아마 될 거예요"를 쓰는 순간 노트북을 만든 이유가 사라진다.

