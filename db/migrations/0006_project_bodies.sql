## 초안은 사람이 안 친다

Claude Code를 subprocess로 띄워서 초안을 만든다. 주제와 뼈대만 주면 1차 원고가 나오고, 거기서부터 내가 말투를 고친다. 영어판은 발행 시점에 자동 번역으로 따라붙는다.

워커도 큐도 안 썼다. 1인 블로그에 분산 처리가 들어가는 순간 취미가 운영이 된다.

