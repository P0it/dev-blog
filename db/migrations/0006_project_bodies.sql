## connect → list → call

흐름을 그대로 화면에 박았다. 단계마다 무엇이 오갔는지 페이로드를 숨기지 않는다. 실패하면 실패한 자리에서 에러를 그대로 보여준다. "왜 안 되지"를 추측하지 않게 하는 게 목표였다.

```mermaid
sequenceDiagram
  participant B as 브라우저
  participant W as CF Worker 프록시
  participant S as MCP 서버
  B->>W: connect(server)
  W->>S: initialize
  S-->>W: tools[]
  W-->>B: list 렌더
  B->>W: call(tool, args)
  W->>S: tools/call
  S-->>B: 날것 응답
```

## 전송 두 개를 한 자리에서

stdio와 sse를 둘 다 시뮬레이션한다. 브라우저는 원래 stdio를 못 붙이니까, Cloudflare Workers를 프록시로 끼웠다. 덕분에 같은 서버를 두 전송으로 번갈아 때려보면서 차이를 눈으로 본다.

스펙에 "SHOULD"라고 적힌 부분이 서버마다 다르게 구현돼 있다는 걸 여기서 처음 알았다. 문서만 봤으면 영영 몰랐을 거다.

